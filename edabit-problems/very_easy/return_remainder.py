#!/usr/bin/env python3


# There is a single operator in Python, capable of providing
# the remainder of a divison operation.
# Two numbers are passed as parameters. The first parameter divided by the second
# parameter will have a remainder, possibly zero. Return that value.

# Examples:
# remainder(1, 3) --> 1
# remainder(3, 4) --> 3

def remainder(x, y):
    return x % y
